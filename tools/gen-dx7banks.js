#!/usr/bin/env node
'use strict';
/* Generate runtime/dx7banks.h: baked DX7 SysEx voice banks.
   Copyrighted DX7 voice data -- the generated header is gitignored.
   Do not hand-edit the header; re-run this script to regenerate. */
const fs   = require('fs');
const path = require('path');

/* Drop your own 32-voice DX7 .syx files into banks/ (gitignored) and list them
   here; `path` is relative to the repo root, and each entry's index is the
   `:bank` number used in patches. The shipped dx patches expect these three:
     bank 0 = DX7 percussion (snares 0-11, kicks 12-22)
     bank 1 = synth percussion / metals
     bank 2 = a melodic cartridge (e.g. the DX7 ROM1A: E.PIANO 1 at preset 10) */
const BANKS = [
  { name: 'kits24', path: 'banks/kits24.syx' },
  { name: 'perc07', path: 'banks/perc07.syx' },
  { name: 'rom1a',  path: 'banks/rom1a.syx' },
];

const VOICES   = 32;
const VOICELEN = 128;
const BODYLEN  = VOICES * VOICELEN; /* 4096 */

function extractBody(buf) {
  let body;
  if (buf[0] === 0xF0) {
    body = buf.slice(6, 6 + BODYLEN);
  } else {
    body = buf.slice(0, BODYLEN);
  }
  if (body.length !== BODYLEN)
    throw new Error(`body is ${body.length} bytes, expected ${BODYLEN}`);
  return body;
}

function voiceName(body, v) {
  /* voice name: bytes [118..128) of each 128-byte voice block */
  const off = v * VOICELEN + 118;
  return Buffer.from(body.slice(off, off + 10))
    .toString('ascii')
    .replace(/[^\x20-\x7e]/g, ' ')
    .trim();
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* generated by tools/gen-dx7banks.js -- do not hand-edit');
lines.push('   source data is copyrighted DX7 voice data, see .gitignore */');
lines.push('');
lines.push('struct Dx7Bank { uint16_t nvoices; const uint8_t* data; const char* name; };');
lines.push('');

const bodies = [];
for (let i = 0; i < BANKS.length; i++) {
  const bank = BANKS[i];
  const buf  = fs.readFileSync(path.resolve(__dirname, '..', bank.path));
  const body = extractBody(buf);
  bodies.push(body);

  const varname = `DX7BANK_${i}`;
  lines.push(`static const uint8_t ${varname}[${BODYLEN}] = {`);
  for (let off = 0; off < BODYLEN; off += 16) {
    const end   = Math.min(off + 16, BODYLEN);
    const chunk = [];
    for (let b = off; b < end; b++) chunk.push(`0x${body[b].toString(16).padStart(2,'0')}`);
    const comma = end < BODYLEN ? ',' : '';
    lines.push('    ' + chunk.join(', ') + comma);
  }
  lines.push('};');
  lines.push('');
}

lines.push(`static const struct Dx7Bank DX7_BANKS[] = {`);
for (let i = 0; i < BANKS.length; i++) {
  const comma = i + 1 < BANKS.length ? ',' : '';
  lines.push(`    { ${VOICES}, DX7BANK_${i}, "${BANKS[i].name}" }${comma}`);
}
lines.push('};');
lines.push('');
lines.push(`#define NUM_DX7_BANKS ${BANKS.length}`);
lines.push('');

const out = path.join(__dirname, '../runtime/dx7banks.h');
fs.writeFileSync(out, lines.join('\n'));
console.log('wrote', out);

/* verification printout */
console.log(`banks: ${BANKS.length}, bytes per bank: ${BODYLEN}`);
for (let i = 0; i < BANKS.length; i++) {
  const n = voiceName(bodies[i], 0);
  console.log(`  [${i}] ${BANKS[i].name}: voice 0 = "${n}"`);
}
