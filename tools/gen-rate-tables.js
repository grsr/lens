'use strict';
// Generate rate_table[256] and ratio_table[256] header files.
//
// rate_table: log-spaced 0.05 Hz .. 20 kHz at 48 kHz SR.
//   rate_table[b] = round(0.05 * (20000/0.05)^(b/255) * 2^32 / 48000)
//
// ratio_table: Q16 log-spaced 2^((b-128)/128), centred at b=128 -> 1.0x.
//   ratio_table[b] = round(2^((b-128)/128) * 65536)

const fs   = require('fs');
const path = require('path');

const SR = 48000;
const N  = 256;

/* rate_table */
const rateTable = new Uint32Array(N);
for (let b = 0; b < N; b++) {
  const hz = 0.05 * Math.pow(20000 / 0.05, b / 255);
  rateTable[b] = Math.round(hz * 4294967296 / SR) >>> 0;
}

/* ratio_table */
const ratioTable = new Uint32Array(N);
for (let b = 0; b < N; b++) {
  ratioTable[b] = Math.round(Math.pow(2, (b - 128) / 128) * 65536) >>> 0;
}

function formatTable(name, arr) {
  const lines = [];
  lines.push('#ifndef __not_in_flash');
  lines.push('#define __not_in_flash(group) /* host build: no-op */');
  lines.push('#endif');
  lines.push(`static const uint32_t __not_in_flash("${name}") ${name}[256] = {`);
  for (let i = 0; i < 256; i += 8) {
    const chunk = [];
    for (let j = i; j < i + 8 && j < 256; j++) {
      chunk.push(arr[j].toString().padStart(10));
    }
    lines.push('    ' + chunk.join(', ') + ',');
  }
  lines.push('};');
  return lines.join('\n');
}

const rateHeader = [
  '#pragma once',
  '#include <stdint.h>',
  '/* Log-spaced phase increments: rate_table[b] = 0.05*(20000/0.05)^(b/255) * 2^32/48000. */',
  '/* Generated by tools/gen-rate-tables.js -- do not edit. */',
  formatTable('rate_table', rateTable),
].join('\n') + '\n';

const ratioHeader = [
  '#pragma once',
  '#include <stdint.h>',
  '/* Q16 log-spaced ratios: ratio_table[b] = 2^((b-128)/128) * 65536. */',
  '/* Generated by tools/gen-rate-tables.js -- do not edit. */',
  formatTable('ratio_table', ratioTable),
].join('\n') + '\n';

const runtimeDir = path.join(__dirname, '../runtime');
fs.writeFileSync(path.join(runtimeDir, 'rate_table.h'), rateHeader);
fs.writeFileSync(path.join(runtimeDir, 'ratio_table.h'), ratioHeader);

console.log('wrote rate_table.h  (256 entries, 0.05 Hz .. 20 kHz)');
console.log('wrote ratio_table.h (256 entries, Q16 log-ratio 2^((b-128)/128))');

/* Spot-check a few entries. */
console.log('rate_table[0]   =', rateTable[0],   ' (expect ~4468 for 0.05 Hz)');
console.log('rate_table[128] =', rateTable[128],  ' (expect ~2081990 for ~23 Hz)');
console.log('rate_table[255] =', rateTable[255],  ' (expect ~1789569706 for 20 kHz)');
console.log('ratio_table[0]  =', ratioTable[0],   ' (expect ~128 for 2^(-1) Q16)');
console.log('ratio_table[128]=', ratioTable[128], ' (expect 65536 for 1.0x)');
console.log('ratio_table[255]=', ratioTable[255], ' (expect ~131072 for ~2x)');
