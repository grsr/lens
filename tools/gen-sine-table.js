'use strict';
/* Generate sine_table.h: two one-cycle sine LUTs scaled to -VMID..+VMID.
   - sine_table[256]: the drum voices' truncating LUT (in RAM via __not_in_flash).
     Matches interp.js: SINE_TABLE[i] = Math.round(sin(2*PI*i/256) * VMID).
   - sine1024[1024]: the oscillators' finer LUT (flash-resident, plain static
     const). 4x the resolution cuts op_sine's linear-interpolation error ~16x so
     heavy FM renders cleaner. Read every sample, served from the XIP cache.
   Written to runtime/sine_table.h. */
const fs = require('fs');
const path = require('path');

const VMID = 2048;
/* The oscillator LUT stores entries at 14-bit amplitude (A14 = 8*VMID) and
   sine_interp scales the interpolated value back to +-VMID at the end. Keeping
   the interpolation high-precision, then rounding once, drops the error to the
   12-bit output quantization floor (the 12-bit-entry table was ~1 LSB above it). */
const A14 = VMID * 8;

function table(n, scale) {
  const e = [];
  for (let i = 0; i < n; i++) e.push(Math.round(Math.sin(2 * Math.PI * i / n) * scale).toString(10));
  return e;
}

function emit(lines, decl, entries, perRow) {
  lines.push(decl);
  for (let i = 0; i < entries.length; i += perRow) {
    const chunk = entries.slice(i, i + perRow).join(', ');
    lines.push('    ' + chunk + (i + perRow < entries.length ? ',' : ''));
  }
  lines.push('};');
  lines.push('');
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* Generated by tools/gen-sine-table.js -- do not edit. */');
lines.push('#ifndef __not_in_flash');
lines.push('#define __not_in_flash(group) /* host build: no-op */');
lines.push('#endif');
lines.push('/* 256-entry sine LUT (drums), one cycle, bipolar -2048..+2048, in RAM. */');
emit(lines, 'static const int16_t __not_in_flash("sine_table") sine_table[256] = {', table(256, VMID), 16);
lines.push('/* 1024-entry sine LUT (oscillators), one cycle, 14-bit amplitude');
lines.push('   (-16384..+16384, scaled to +-2048 in sine_interp), flash-resident. */');
emit(lines, 'static const int16_t sine1024[1024] = {', table(1024, A14), 16);

const out = path.join(__dirname, '../runtime/sine_table.h');
fs.writeFileSync(out, lines.join('\n'));
console.log('wrote', out);
