'use strict';
/* Generate sine_table.h: 256-entry int16 LUT, one cycle, scaled to -VMID..+VMID.
   Matches interp.js exactly: SINE_TABLE[i] = Math.round(sin(2*PI*i/256) * VMID).
   Written to runtime/sine_table.h. */
const fs = require('fs');
const path = require('path');

const VMID = 2048;
const entries = [];
for (let i = 0; i < 256; i++) {
  const v = Math.round(Math.sin(2 * Math.PI * i / 256) * VMID);
  entries.push(v.toString(10));
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* 256-entry sine LUT, one cycle, bipolar -2048..+2048. */');
lines.push('/* Generated by tools/gen-sine-table.js -- do not edit. */');
lines.push('#ifndef __not_in_flash');
lines.push('#define __not_in_flash(group) /* host build: no-op */');
lines.push('#endif');
lines.push('static const int16_t __not_in_flash("sine_table") sine_table[256] = {');
for (let i = 0; i < 256; i += 16) {
  const chunk = entries.slice(i, i + 16).join(', ');
  lines.push('    ' + chunk + (i + 16 < 256 ? ',' : ''));
}
lines.push('};');
lines.push('');

const out = path.join(__dirname, '../runtime/sine_table.h');
fs.writeFileSync(out, lines.join('\n'));
console.log('wrote', out);
