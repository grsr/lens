'use strict';
/* Generate runtime/fm_algorithms.h: DX7 algorithm routing table for op_dx.
   Mirror of tools/gen-pitch-table.js style. */
const fs = require('fs');
const path = require('path');
const { ALGORITHMS } = require('./dx7import');

const results = [];

for (let algo = 1; algo <= 32; algo++) {
    const alg = ALGORITHMS[algo];
    const edges = alg.edges || [];
    const feedbackPair = alg.feedback;
    const fbop1 = feedbackPair ? feedbackPair[1] : 0;

    /* modmask[op] (0-indexed): 6-bit mask of which ops modulate this op */
    const modmask = new Array(6).fill(0);
    for (const [from, to] of edges) {
        if (from !== to) {
            modmask[to - 1] |= (1 << (from - 1));
        }
    }

    /* modcount[op]: popcount(modmask[op]) */
    const modcount = modmask.map(m => {
        let c = 0, x = m;
        while (x) { c += x & 1; x >>>= 1; }
        return c;
    });

    /* carriers: 6-bit mask (bit i = operator i+1 is a carrier) */
    let carrierMask = 0;
    for (const c of alg.carriers) carrierMask |= (1 << (c - 1));
    const ncarriers = alg.carriers.length;

    /* fbop: 0-indexed self-feedback operator */
    const fbop = fbop1 > 0 ? fbop1 - 1 : -1;

    /* Post-order DFS: mirrors dx7import.js emit(); modulators before self */
    const order = [];
    const done = new Set();

    const dfs = n => {
        if (done.has(n)) return;
        done.add(n);  /* mark first to break self-cycles */
        const mods = edges.filter(e => e[1] === n && e[0] !== n).map(e => e[0]);
        mods.forEach(dfs);
        order.push(n - 1);  /* 0-indexed */
    };

    alg.carriers.forEach(dfs);

    /* Append any operators not reachable from carriers */
    for (let i = 1; i <= 6; i++) {
        if (!done.has(i)) order.push(i - 1);
    }

    results.push({ order, modmask, modcount, carriers: carrierMask, ncarriers, fbop });
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* DX7 algorithm routing table. Generated by tools/gen-fm-algorithms.js -- do not edit. */');
lines.push('');
lines.push('struct FmAlgo {');
lines.push('    int8_t   order[6];      /* evaluation order, modulators before carriers */');
lines.push('    uint8_t  modmask[6];    /* 6-bit mask: which operators modulate this one */');
lines.push('    uint8_t  modcount[6];   /* popcount(modmask[op]) */');
lines.push('    uint8_t  carriers;      /* 6-bit mask of carrier operators */');
lines.push('    uint8_t  ncarriers;     /* number of carriers */');
lines.push('    int8_t   fbop;          /* 0-indexed self-feedback operator, or -1 */');
lines.push('};');
lines.push('');
lines.push('static const struct FmAlgo FM_ALGOS[32] = {');

for (let i = 0; i < 32; i++) {
    const r = results[i];
    const order   = r.order.join(',');
    const modmask = r.modmask.join(',');
    const modcount= r.modcount.join(',');
    lines.push(`    /* algo ${i+1} */ { .order={${order}}, .modmask={${modmask}}, .modcount={${modcount}}, .carriers=${r.carriers}, .ncarriers=${r.ncarriers}, .fbop=${r.fbop} },`);
}

lines.push('};');
lines.push('');

const out = path.join(__dirname, '../runtime/fm_algorithms.h');
fs.writeFileSync(out, lines.join('\n'));
console.log('wrote', out);
