'use strict';
/* Generate pitch_table.h: 128-entry MIDI->phase-increment LUT at 48kHz.
   phase_inc = hz/SR * 2^32  where hz = 440 * 2^((n-69)/12).
   Written to runtime/pitch_table.h. */
const fs = require('fs');
const path = require('path');

const SR = 48000;
const entries = [];
for (let n = 0; n < 128; n++) {
  const hz = 440 * Math.pow(2, (n - 69) / 12);
  const inc = Math.round(hz / SR * 4294967296);
  entries.push((inc >>> 0).toString(10));
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* MIDI note 0..127 -> 32-bit phase increment per sample at 48 kHz. */');
lines.push('/* Generated by tools/gen-pitch-table.js -- do not edit. */');
lines.push('#ifndef __not_in_flash');
lines.push('#define __not_in_flash(group) /* host build: no-op */');
lines.push('#endif');
lines.push('static const uint32_t __not_in_flash("pitch_table") pitch_table[128] = {');
for (let i = 0; i < 128; i += 8) {
  const chunk = entries.slice(i, i + 8).join(', ');
  lines.push('    ' + chunk + (i + 8 < 128 ? ',' : ''));
}
lines.push('};');
lines.push('');

const out = path.join(__dirname, '../runtime/pitch_table.h');
fs.writeFileSync(out, lines.join('\n'));
console.log('wrote', out);
