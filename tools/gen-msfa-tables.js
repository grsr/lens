'use strict';
/* Generate runtime/msfa_tables.h: the two data tables the faithful op_dx port
   needs, bit-exact ports of Dexed/msfa (attic/dx7ref/sin.cc, freqlut.cc).

   - msfa_sintab[2048]: 24-bit sine, SIN_DELTA layout (interleaved [delta,value]),
     SIN_LG_N_SAMPLES=10 -> 1024 cycle samples. Phase is 2^24/cycle; lookup
     returns ~+-2^24 amplitude. Ports Sin::init() exactly (int64 recurrence, so
     this generator uses BigInt to match C two's-complement arithmetic shifts).
   - msfa_freqlut[1025]: logfreq (Q24, 1.0 = one octave) -> phase increment at
     48 kHz. Ports Freqlut::init(48000.0); pure double math.

   Verify against the C source with: make msfa-tables-test (test-runner/), which
   compiles the real sin.cc/freqlut.cc and diffs every entry.
   Written to runtime/msfa_tables.h. */
const fs = require('fs');
const path = require('path');

const SIN_LG_N_SAMPLES = 10;
const SIN_N_SAMPLES = 1 << SIN_LG_N_SAMPLES; // 1024
const SAMPLE_RATE = 48000;                   // matches Lens + dx7ref main.cc
const MAX_LOGFREQ_INT = 20;

/* Port of Sin::init() (sin.cc) with SIN_DELTA defined. BigInt throughout so the
   int64 products and arithmetic right-shifts match C on two's-complement. */
function sinTable() {
  const N = SIN_N_SAMPLES;
  const tab = new Array(N << 1).fill(0n);
  const R = 1n << 29n;
  const dphase = (2 * Math.PI) / N;
  // c, s computed in double then floored, exactly as the C does.
  const c = BigInt(Math.floor(Math.cos(dphase) * (1 << 30) + 0.5));
  const s = BigInt(Math.floor(Math.sin(dphase) * (1 << 30) + 0.5));
  let u = 1n << 30n;
  let v = 0n;
  for (let i = 0; i < N / 2; i++) {
    const val = (v + 32n) >> 6n;            // BigInt >> is arithmetic (floor)
    tab[(i << 1) + 1] = val;
    tab[((i + N / 2) << 1) + 1] = -val;
    const t = (u * s + v * c + R) >> 30n;
    u = (u * c - v * s + R) >> 30n;
    v = t;
  }
  for (let i = 0; i < N - 1; i++) {
    tab[i << 1] = tab[(i << 1) + 3] - tab[(i << 1) + 1];
  }
  tab[(N << 1) - 2] = -tab[(N << 1) - 1];
  return tab.map((x) => Number(x)); // all fit in 32-bit signed
}

/* Port of Freqlut::init(sample_rate) (freqlut.cc). Plain double math. */
function freqlutTable() {
  const N = SIN_N_SAMPLES;
  const lut = new Array(N + 1);
  let y = Math.pow(2, 24 + MAX_LOGFREQ_INT) / SAMPLE_RATE; // 1<<44 fits in double exactly
  const inc = Math.pow(2, 1.0 / N);
  for (let i = 0; i < N + 1; i++) {
    lut[i] = Math.floor(y + 0.5);
    y *= inc;
  }
  return lut;
}

function emit(lines, decl, entries, perRow) {
  lines.push(decl);
  for (let i = 0; i < entries.length; i += perRow) {
    const chunk = entries.slice(i, i + perRow).map((x) => String(x)).join(', ');
    lines.push('    ' + chunk + (i + perRow < entries.length ? ',' : ''));
  }
  lines.push('};');
  lines.push('');
}

const lines = [];
lines.push('#pragma once');
lines.push('#include <stdint.h>');
lines.push('/* Generated by tools/gen-msfa-tables.js -- do not edit.');
lines.push('   Bit-exact ports of Dexed/msfa sin.cc + freqlut.cc (48 kHz). */');
lines.push('');
lines.push('#define MSFA_SIN_LG_N 10');
lines.push('#define MSFA_SIN_N    (1 << MSFA_SIN_LG_N)   /* 1024 */');
lines.push('#define MSFA_MAX_LOGFREQ_INT 20');
lines.push('');
lines.push('/* 24-bit sine, SIN_DELTA layout: interleaved [delta, value] per sample.');
lines.push('   Phase 2^24/cycle; output ~ +-2^24. */');
emit(lines, 'static const int32_t msfa_sintab[MSFA_SIN_N << 1] = {', sinTable(), 8);
lines.push('/* logfreq (Q24, 1.0=octave) -> phase increment at 48 kHz. */');
emit(lines, 'static const int32_t msfa_freqlut[MSFA_SIN_N + 1] = {', freqlutTable(), 8);

lines.push('/* High 32 bits of an unsigned 32x32 product. The freqlut lookup below needs');
lines.push('   it to stay 32-bit (no __aeabi_lmul). msfa_op.h reuses this one too. */');
lines.push('static inline uint32_t msfa_umulhi32(uint32_t a, uint32_t b) {');
lines.push('  uint32_t ah = a >> 16, al = a & 0xFFFF, bh = b >> 16, bl = b & 0xFFFF;');
lines.push('  uint32_t lo = al * bl;');
lines.push('  uint32_t m1 = ah * bl + (lo >> 16);');
lines.push('  uint32_t m2 = al * bh + (m1 & 0xFFFF);');
lines.push('  return ah * bh + (m1 >> 16) + (m2 >> 16);');
lines.push('}');
lines.push('');
lines.push('/* Faithful Sin::lookup (sin.cc, SIN_DELTA). phase Q24/cycle. dy*lowbits is');
lines.push('   ~31-bit (|dy|<=102943, lowbits<2^14) so one signed 32-bit product suffices. */');
lines.push('static inline int32_t msfa_sin_lookup(int32_t phase) {');
lines.push('  const int SHIFT = 24 - MSFA_SIN_LG_N;');
lines.push('  int lowbits = phase & ((1 << SHIFT) - 1);');
lines.push('  int phase_int = (phase >> (SHIFT - 1)) & ((MSFA_SIN_N - 1) << 1);');
lines.push('  int32_t dy = msfa_sintab[phase_int];');
lines.push('  int32_t y0 = msfa_sintab[phase_int + 1];');
lines.push('  return y0 + ((dy * lowbits) >> SHIFT);');
lines.push('}');
lines.push('');
lines.push('/* Faithful Freqlut::lookup (freqlut.cc). logfreq Q24, 1.0=octave. The table is');
lines.push('   monotonic so (y1-y0)>=0; the >>SAMPLE_SHIFT of a ~33-bit product is taken');
lines.push('   from its two 32-bit halves (msfa_umulhi32), staying 32-bit. */');
lines.push('static inline int32_t msfa_freqlut_lookup(int32_t logfreq) {');
lines.push('  const int SAMPLE_SHIFT = 24 - MSFA_SIN_LG_N;');
lines.push('  int ix = (logfreq & 0xffffff) >> SAMPLE_SHIFT;');
lines.push('  int32_t y0 = msfa_freqlut[ix];');
lines.push('  int32_t y1 = msfa_freqlut[ix + 1];');
lines.push('  uint32_t d = (uint32_t)(y1 - y0);');
lines.push('  uint32_t lowbits = (uint32_t)(logfreq & ((1 << SAMPLE_SHIFT) - 1));');
lines.push('  uint32_t prod_lo = d * lowbits;');
lines.push('  uint32_t prod_hi = msfa_umulhi32(d, lowbits);');
lines.push('  int32_t y = y0 + (int32_t)((prod_hi << (32 - SAMPLE_SHIFT)) | (prod_lo >> SAMPLE_SHIFT));');
lines.push('  int hibits = logfreq >> 24;');
lines.push('  return y >> (MSFA_MAX_LOGFREQ_INT - hibits);');
lines.push('}');
lines.push('');

